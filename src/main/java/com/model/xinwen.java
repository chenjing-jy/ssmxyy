package com.model;

/**
 * 新闻 bean方法
 */
public class xinwen {

    private String xwid = "";//新闻编号

    private String bt = "";//标题

    private String nr = "";//内容

    private String fbsj = "";//发布时间


    public String getXwid() {
        return xwid;
    }

    public void setXwid(String xwid) {
        this.xwid = xwid;
    }

    public String getBt() {
        return bt;
    }

    public void setBt(String bt) {
        this.bt = bt;
    }

    public String getNr() {
        return nr;
    }

    public void setNr(String nr) {
        this.nr = nr;
    }

    public String getFbsj() {
        return fbsj;
    }

    public void setFbsj(String fbsj) {
        this.fbsj = fbsj;
    }


}
